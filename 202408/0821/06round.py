# define multiple var

ch = 12.3456
print(ch)
print('%d' %(ch))   # 정수까지만 출력
print('%f' %(ch))   # 실수 소숫점 아래 6자리까지 출력
print('%.2f' %(ch)) # 실수 소숫점 아래 2자리까지 출력
print(round(ch,2))  #내장함수 round(x,n) // (값, 소숫점 아래 N자리까지 출력)

